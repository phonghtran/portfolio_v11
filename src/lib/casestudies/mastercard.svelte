<h3>Context</h3>
<p>
	It’s one of those projects where it’s less about the UX and designs, but more about the approach
	and meeting the client where they are. The client team had a vision for a streamlined internal
	process of data ingest and visualization. But the existing team was having trouble translating the
	requirements and desires into designs that matched the clients’ wants.
</p>
<h3>Dropping In</h3>
<p>
	With the client super frustrated, it was thought I could try leading the project and bring in a
	fresh perspective. Instead of redo all the workshop activities and re-interviewing all the
	stakeholders again, I decided to dive right in. On our virtual calls, I opened up Figma and just
	let the describe how they saw the project and features. While they talked I attempted to sketch
	out the systems and flows they were discussing. It helped that I had a technical background and
	could understand database systems and ETL flows.
</p>

<p>
	Each discussion, we had sketches and workflows that they felt happy with. More importantly, I
	learned their trust to keep the talks going. As I gained more confident that I was interpreting
	their vision correctly, I would work with our designers to work out screens to flesh out. Those
	would be shown to the client, get approved, and we would then move to the next set of screens to
	sketch out live. Each day would be a bit different as I would drive them towards either wireframes
	or user stories or screens to react to.
</p>
<p>
	Figma prototypes doubled as slide presentations as we were already in those files so intensely
	everyday. And we could link dynamically each workflow as it matured and was worked on.
</p>

